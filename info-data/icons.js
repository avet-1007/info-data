function fileIcon(path) {
  if (/\.(png|jpe?g|gif|svg|webp|bmp|ico)$/i.test(path)) return { icon: 'image', color: '#c678dd' };
  const lang = (typeof FileTypes !== 'undefined') ? FileTypes.detect(path) : 'text';
  return FILE_ICONS[lang] || { icon: 'file', color: '#9aa0a6' };
}

