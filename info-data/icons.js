function Icon(name, opts = {}) {
  const inner = ICONS[name] || ICONS.file;
  const size = opts.size || 16;
  const cls = opts.cls ? ` class="${opts.cls}"` : '';
  const style = opts.color ? ` style="color:${opts.color}"` : '';
  return `<svg${cls}${style} width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${inner}</svg>`;
}

